#!/usr/bin/env bash
# Wallpaper subsystem: writes the launcher script and wires it into ~/.xinitrc
# inside a marked, idempotent block.

WP_BLOCK_START="# >>> manjaro-sl wallpaper >>>"
WP_BLOCK_END="# <<< manjaro-sl wallpaper <<<"

# Single source of truth for desktop wallpapers. A registry entry may exist
# before its program ships — consumers gate on available_wallpapers (directory
# exists) so unbuilt names never surface in menus or validation.
KNOWN_WALLPAPERS=(doomfire xmatrix xcolormix xgameoflife xblackhole
                  xstarfield xplasma xrain xfireflies)
declare -gA WALLPAPER_DESCS=(
  [doomfire]="DOOM fire X11 wallpaper animation"
  [xmatrix]="Matrix rain X11 wallpaper animation"
  [xcolormix]="Shifting color gradients wallpaper animation"
  [xgameoflife]="Conway's Game of Life wallpaper animation"
  [xblackhole]="Black hole accretion-disk wallpaper animation"
  [xstarfield]="Flying starfield wallpaper animation"
  [xplasma]="Demoscene plasma wallpaper animation"
  [xrain]="Falling rain wallpaper animation"
  [xfireflies]="Drifting fireflies wallpaper animation"
)

is_known_wallpaper() {
  local w
  for w in "${KNOWN_WALLPAPERS[@]}"; do [ "$w" = "$1" ] && return 0; done
  return 1
}

available_wallpapers() {
  local w
  for w in "${KNOWN_WALLPAPERS[@]}"; do
    [ -d "$REPO_ROOT/$w" ] && echo "$w"
  done
}

ly_animation_to_wallpaper() {
  case "$1" in
    doom)   echo doomfire ;;
    matrix) echo xmatrix ;;
    *)      echo none ;;   # colormix is a phase-3 stub
  esac
}

wallpaper_write_launcher() {
  local wp="$1" dir="$HOME/.config/manjaro-sl" f
  f="$dir/wallpaper.sh"
  mkdir -p "$dir"
  cat > "$f" <<EOF
#!/usr/bin/env bash
# Generated by manjaro-sl — starts the desktop wallpaper animation.
exec $wp
EOF
  chmod 755 "$f"
}

wallpaper_strip_block() {
  local xi="$HOME/.xinitrc"
  [ -f "$xi" ] || return 0
  sed -i "\|^${WP_BLOCK_START}\$|,\|^${WP_BLOCK_END}\$|d" "$xi"
}

wallpaper_wire_xinitrc() {
  local xi="$HOME/.xinitrc"
  wallpaper_strip_block
  touch "$xi"
  # Insert before an 'exec dwm' line if present, else append.
  local block
  block=$(printf '%s\n%s\n%s' "$WP_BLOCK_START" \
    "\"\$HOME/.config/manjaro-sl/wallpaper.sh\" &" "$WP_BLOCK_END")
  if grep -q '^exec .*dwm' "$xi"; then
    # The rewrite must keep the original mode: Ly executes ~/.xinitrc as a
    # program, so losing the +x bit locks the user out of their session.
    local mode; mode=$(stat -c '%a' "$xi")
    awk -v blk="$block" '!done && /^exec .*dwm/ { print blk; done=1 } { print }' \
      "$xi" > "$xi.tmp" && mv "$xi.tmp" "$xi"
    chmod "$mode" "$xi"
  else
    [ -s "$xi" ] && [ -n "$(tail -c1 "$xi")" ] && printf '\n' >> "$xi"
    printf '%s\n' "$block" >> "$xi"
  fi
}

wallpaper_apply() {
  local wp; wp=$(state_get dwm/wallpaper)
  if [ "$wp" = "none" ] || [ -z "$wp" ] || [ "$wp" = "off" ]; then
    wallpaper_strip_block
    return 0
  fi
  wallpaper_write_launcher "$wp"
  wallpaper_wire_xinitrc
}
